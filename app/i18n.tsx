export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<CVLocale>('zh-TW')
  const [mounted, setMounted] = useState(false)

  useEffect(() => {
    if (typeof window !== 'undefined') {
      const browserLang = navigator.language || (navigator as any).userLanguage || 'zh-TW'
      const lang = browserLang.split('-')[0].toLowerCase()
      
      const langMap: Record<string, CVLocale> = {
        'zh': 'zh-TW', 'en': 'en', 'ja': 'ja', 'ko': 'ko',
        'fr': 'fr', 'de': 'de', 'pt': 'pt', 'es': 'es', 'th': 'th'
      }
      
      setLocale(langMap[lang] || 'zh-TW')
      setMounted(true)
    }
  }, [])

  const t = (key: string): string => {
    return translations[locale]?.[key] || translations['zh-TW'][key] || key
  }

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}
